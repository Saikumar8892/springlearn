package com.day01;

public class Pen 
{
   private int inkQty;
   private String name;
   
   //getter setter method
   
   public void setName(String name)
   {
	 this.name = name;
   }
   
   public String getName()
   {
	   return this.name;
   }
   
   public void setInkQty(int inkQty)
   {
	   this.inkQty = inkQty;
   }
   
   public int getInkQty()
   {
	   return this.inkQty;
   }
}
